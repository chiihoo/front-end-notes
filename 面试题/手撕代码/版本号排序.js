arr.sort((a, b) => {
  // 第一次比较a是'2.3.3'，b是'0.1.1'
  // 最后返回大于0，则两者不调换位置；返回小于0，则两者调换位置
  let t1 = a.split('.')
  let t2 = b.split('.')
  let i = 0
  while (1) {
    if (t1 === undefined || t2 === undefined) {
      return t2.length - t1.length
    }
    if (t1[i] !== t2[i]) {
      return t2[i] - t1[i]
    }
    i++
  }
})
